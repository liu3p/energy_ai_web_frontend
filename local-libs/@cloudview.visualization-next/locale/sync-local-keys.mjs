const compareKeys = (sourceData, targetData) => {
    const keys = Object.keys(sourceData);
    keys.forEach(key => {
        if (typeof sourceData[key] === 'string') {
            if (!targetData.hasOwnProperty(key)) {
                targetData[key] = sourceData[key];
            }
        } else {
            compareKeys(
                sourceData[key],
                targetData[key] ? targetData[key] : (targetData[key] = {})
            );
        }
    });
    const targetKeys = Object.keys(targetData);
    targetKeys.forEach(key => {
        if (!sourceData.hasOwnProperty(key)) {
            Reflect.deleteProperty(targetData, key)
        }
    })
};

